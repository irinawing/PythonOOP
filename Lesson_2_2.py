def fib(n):
    """
    Рекурсивно вычисляет n-е число Фибоначчи
    для a0 = 0, a1 = 1, ak = a_{k-1} + a_{k-2}.
    """
    if n <= 1:
        return n
    return fib(n - 1) + fib(n - 2)

if __name__ == "__main__":
    # Считываем N
    n = int(input("Введите N: "))
    # По примеру цикла из предыдущей задачи
    # выводили fib(n+1), чтобы для N=7 получить 21
    result = fib(n + 1)
    print(result)
