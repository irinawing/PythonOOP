orig = '3 4 5 6 7'

def func_2(s):
    """
    Рекурсивно возвращает обратную строку s.
    Без использования циклов.
    """
    # Базовый случай: пустая строка
    if s == "":
        return s
    # Рекурсивно: последний символ + реверс оставшейся строки
    return s[-1] + func_2(s[:-1])

print(func_2(orig))
