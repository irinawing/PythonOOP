def dict_get(dictionary, key, default=None):
    """
    –ê–Ω–∞–ª–æ–≥ –º–µ—Ç–æ–¥–∞ get() –¥–ª—è —Å–ª–æ–≤–∞—Ä—è.

    :param dictionary: –°–ª–æ–≤–∞—Ä—å, –∏–∑ –∫–æ—Ç–æ—Ä–æ–≥–æ –ø–æ–ª—É—á–∞–µ–º –∑–Ω–∞—á–µ–Ω–∏–µ.
    :param key: –ö–ª—é—á, –∑–Ω–∞—á–µ–Ω–∏–µ –∫–æ—Ç–æ—Ä–æ–≥–æ –Ω—É–∂–Ω–æ –ø–æ–ª—É—á–∏—Ç—å.
    :param default: –ó–Ω–∞—á–µ–Ω–∏–µ –ø–æ —É–º–æ–ª—á–∞–Ω–∏—é, –µ—Å–ª–∏ –∫–ª—é—á –Ω–µ —Å—É—â–µ—Å—Ç–≤—É–µ—Ç (–ø–æ —É–º–æ–ª—á–∞–Ω–∏—é None).
    :return: –ó–Ω–∞—á–µ–Ω–∏–µ –∫–ª—é—á–∞ –∏–ª–∏ default, –µ—Å–ª–∏ –∫–ª—é—á–∞ –Ω–µ—Ç.
    """
    try:
        return dictionary[key]
    except KeyError:
        return default


