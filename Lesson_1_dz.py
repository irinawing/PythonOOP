def even_generator(n):
    """
    Генератор четных чисел от 0 до n включительно.

    :param n: верхняя граница (целое неотрицательное)
    :yield: очередное четное число
    """
    # Проходим по диапазону с шагом 2, начиная с 0
    for x in range(0, n + 1, 2):
        yield x


# Пример использования
if __name__ == "__main__":
    n = int(input("Введите n: "))
    print("Четные числа от 0 до", n)
    for even in even_generator(n):
        print(even)
