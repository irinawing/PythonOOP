"""
Задание 1.

Напишите рекурсивную функцию sum(a, b),
возвращающую сумму двух целых неотрицательных чисел.
Из всех арифметических операций допускаются только +1 и -1.
Также нельзя использовать циклы.

Функция не должна ничего выводить, только возвращать значение.
"""

def sum(a, b):
    """
    Рекурсивно вычисляет сумму двух целых неотрицательных чисел a и b,
    используя только операции +1 и -1 и без циклов.
    """
    # Базовый случай: если второй операнд 0, результат равен первому
    if b == 0:
        return a
    # Рекурсивный шаг: прибавляем 1 к a и вычитаем 1 из b
    return sum(a + 1, b - 1)

# Пример использования
if __name__ == "__main__":
    print(sum(5, 7))   # выведет 12
    print(sum(0, 10))  # выведет 10
    print(sum(3, 0))   # выведет 3
